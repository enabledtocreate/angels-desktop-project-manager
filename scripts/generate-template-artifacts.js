function operationsBlock(docType, targetSections) {
  const targetDescriptor = Array.isArray(targetSections) && targetSections.length
    ? targetSections.join('|')
    : 'section_a|section_b';
  const extraLines = operationExtraLinesForDocType(docType);
  const itemLines = operationItemLinesForDocType(docType);
  return [
    '<!-- APM:OPERATIONS',
    '[',
    '  {',
    '    "operation": "{{OPERATION:add|update|remove|reorder|move|link|unlink}}",',
    `    "targetSection": "{{TARGET_SECTION:${targetDescriptor}}}",`,
    '    "fromSection": "{{FROM_SECTION:0..1}}",',
    '    "targetItemId": "{{TARGET_ITEM_ID:0..1}}",',
    '    "beforeItemId": "{{BEFORE_ITEM_ID:0..1}}",',
    '    "afterItemId": "{{AFTER_ITEM_ID:0..1}}",',
    '    "orderedIds": {{ORDERED_IDS_JSON:0..N}},',
    '    "versionDate": "{{VERSION_DATE:0..1}}",',
    ...withTrailingCommas(extraLines),
    '    "item": {',
    ...withTrailingCommas(itemLines),
    '    },',
    '    "sourceRefs": {{SOURCE_REFS_JSON:0..N}}',
    '  }{{ADDITIONAL_OPERATION_BLOCK:0..N}}',
    ']',
    '-->',
  ];
}

function buildDocumentTemplate({ title, templateName, docType, managedProps = [], managedBlockLines = null, bodyLines }) {
  return [
    title,
    '',
    `> Managed document. Must comply with template ${templateName}.`,
    '',
    ...(Array.isArray(managedBlockLines) && managedBlockLines.length
      ? managedBlockLines
      : docManagedBlock(docType, templateName, managedProps)),
    '',
    ...bodyLines,
    '',
  ].join('\n');
}

function buildFragmentTemplate({ title, templateName, docType, targetSections, fragmentProps = [], managedBlockLines = null, bodyLines }) {
  const mergeMode = fragmentMergeModeForDocType(docType);
  return [
    title,
    '',
    `> Managed document. Must comply with template ${templateName}.`,
    '',
    ...(Array.isArray(managedBlockLines) && managedBlockLines.length
      ? managedBlockLines
      : genericFragmentManagedBlock(docType, templateName, fragmentProps)),
    ...(mergeMode === 'apm-operations' ? [
      '',
      ...operationsBlock(docType, targetSections),
    ] : []),
    '',
    ...bodyLines,
    '',
  ].join('\n');
}

function buildCustomManagedBlock(lines = []) {
  return [
    '<!-- APM:DATA',
    ...lines,
    '-->',
  ];
}

