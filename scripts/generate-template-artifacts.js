function buildTemplateSection(templateName, templateVersion, content, role, extraLines = []) {
  const placeholders = extractPlaceholders(content);
  const lines = [
    `### ${templateName}`,
    '',
    `- Template Version: \`${templateVersion}\``,
    `- Last Updated: \`${today}\``,
    `- Template Role: \`${role}\``,
    ...extraLines,
    '- Fill-In Slots:',
  ];
  if (!placeholders.length) {
    lines.push('  - None.');
  } else {
    placeholders.forEach((placeholder) => {
      lines.push(`  - \`{{${placeholder}}}\`: ${formatSlotDescription(placeholder)}`);
    });
  }
  lines.push('');
  return lines.join('\n');
}

for (const config of Object.values(moduleConfigs)) {
  const docVersion = bumpVersion(currentTemplateVersion(config.docTemplate.file));
  const fragmentVersion = bumpVersion(currentTemplateVersion(config.fragmentTemplate.file));
  const aiVersion = bumpVersion(currentAiVersion(config.aiFile));

  const docContent = buildDocumentTemplate({
    title: config.docTemplate.title,
    templateName: config.docTemplate.file,
    docType: config.docType,
    managedProps: config.docTemplate.managedProps,
    managedBlockLines: config.docTemplate.managedBlockLines,
    bodyLines: config.docTemplate.bodyLines,
  });

  const fragmentContent = buildFragmentTemplate({
    title: `# ${config.label} Fragment: {{FRAGMENT_CODE}} - {{FRAGMENT_TITLE}}`,
    templateName: config.fragmentTemplate.file,
    docType: config.fragmentTemplate.docType,
    targetSections: config.fragmentTemplate.targetSections,
    fragmentProps: config.fragmentTemplate.fragmentProps,
    managedBlockLines: config.fragmentTemplate.managedBlockLines,
    bodyLines: config.fragmentTemplate.bodyLines,
  });

  fs.writeFileSync(path.join(templatesDir, config.docTemplate.file), docContent);
  fs.writeFileSync(path.join(templatesDir, config.fragmentTemplate.file), fragmentContent);

  const aiPath = path.join(templatesDir, config.aiFile);
  let aiContent = readFileSafe(aiPath);
  aiContent = aiContent
    .replace(/(AI File Version:\s*`)([^`]+)(`)/i, `$1${aiVersion}$3`)
    .replace(/(Last Updated:\s*`)([^`]+)(`)/i, `$1${today}$3`);

  const templateRulesSection = [
    '## 9. Template Construction Rules',
    '',
    '### Placeholder Syntax',
    '',
    '- `{{NAME}}`: required single value.',
    '- `{{NAME:OPTION_A|OPTION_B}}`: select one allowed value.',
    '- `{{NAME:0..1}}`: optional value or optional block.',
    '- `{{NAME:0..N}}`: repeatable value, list, array, or repeated markdown block.',
    '- Placeholders used as full JSON values must be replaced with valid JSON objects, arrays, strings, numbers, booleans, or `null` that match the surrounding structure.',
    '- `<!-- REPEAT {{NAME:0..N}} --> ... <!-- END REPEAT {{NAME}} -->` marks a repeatable markdown region.',
    '- Keep the `APM:DATA` and `APM:OPERATIONS` blocks structurally valid after replacement.',
    '',
    buildTemplateSection(config.docTemplate.file, docVersion, docContent, 'document', [
      `- Generated Artifact: \`${config.docTemplate.file.replace('.template.md', '.md')}\``,
      '- Consumption Goal: A filled template should read like the final managed document body and keep the managed metadata block intact.',
    ]),
    buildTemplateSection(config.fragmentTemplate.file, fragmentVersion, fragmentContent, 'fragment', [
      `- Fragment Merge Mode: \`${fragmentMergeModeForDocType(config.fragmentTemplate.docType)}\``,
      ...(fragmentMergeModeForDocType(config.fragmentTemplate.docType) === 'apm-operations'
        ? [`- Allowed Target Sections: ${config.fragmentTemplate.targetSections.map((value) => `\`${value}\``).join(', ')}`]
        : []),
      fragmentMergeModeForDocType(config.fragmentTemplate.docType) === 'apm-operations'
        ? '- Consumption Goal: A filled template should preserve valid fragment metadata and a valid operations block for merge-time processing.'
        : fragmentMergeModeForDocType(config.fragmentTemplate.docType) === 'database-import'
          ? '- Consumption Goal: A filled template should preserve valid fragment metadata and a complete schema payload suitable for import.'
          : '- Consumption Goal: A filled template should preserve valid fragment metadata and body sections that the current fragment importer reads directly.',
    ]),
  ].join('\n');

  if (/## 9\. Template Construction Rules[\s\S]*$/m.test(aiContent)) {
    aiContent = aiContent.replace(/## 9\. Template Construction Rules[\s\S]*$/m, templateRulesSection);
  } else {
    aiContent = `${aiContent.trim()}\n\n${templateRulesSection}`;
  }

  fs.writeFileSync(aiPath, aiContent.replace(/\n{3,}/g, '\n\n').trim() + '\n');
}

