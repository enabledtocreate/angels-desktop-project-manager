function readFileSafe(filePath) {
  return fs.existsSync(filePath) ? fs.readFileSync(filePath, 'utf8') : '';
}

function bumpVersion(version) {
  const match = String(version || '').trim().match(/^(\d+)(?:\.(\d+))?$/);
  if (!match) return '1.0';
  const major = Number(match[1] || 1);
  const minor = Number(match[2] || 0) + 1;
  return `${major}.${minor}`;
}

function getAiFileNameForTemplate(templateName) {
  const raw = String(templateName || '').trim();
  if (!raw) return '';
  if (raw.endsWith('_FRAGMENT.template.md')) return raw.replace('_FRAGMENT.template.md', '.ai.md');
  if (raw.endsWith('.template.md')) return raw.replace('.template.md', '.ai.md');
  return '';
}

function currentTemplateVersion(templateName) {
  const aiFileName = getAiFileNameForTemplate(templateName);
  const aiText = aiFileName ? readFileSafe(path.join(templatesDir, aiFileName)) : '';
  if (aiText) {
    const lines = aiText.split(/\r?\n/);
    const heading = `### ${String(templateName || '').trim()}`;
    let sectionStart = -1;
    for (let index = 0; index < lines.length; index += 1) {
      if (String(lines[index] || '').trim() === heading) {
        sectionStart = index + 1;
        break;
      }
    }
    if (sectionStart !== -1) {
      const sectionLines = [];
      for (let index = sectionStart; index < lines.length; index += 1) {
        const line = String(lines[index] || '');
        if (/^##\s+/.test(line) || /^###\s+/.test(line)) break;
        sectionLines.push(line);
      }
      const sectionText = sectionLines.join('\n');
      const aiMatch = sectionText.match(/Template Version:\s*`([^`]+)`/i);
      if (aiMatch) return aiMatch[1];
    }
  }
  const text = readFileSafe(path.join(templatesDir, templateName));
  const match = text.match(/Template Version:\s*`([^`]+)`/i);
  return match ? match[1] : '1.0';
}

function currentAiVersion(aiFileName) {
  const text = readFileSafe(path.join(templatesDir, aiFileName));
  const match = text.match(/AI File Version:\s*`([^`]+)`/i);
  return match ? match[1] : '1.0';
}

function parsePlaceholder(raw) {
  const text = String(raw || '').trim();
  const separatorIndex = text.indexOf(':');
  if (separatorIndex === -1) return { raw: text, name: text, descriptor: '', options: [] };
  const name = text.slice(0, separatorIndex).trim();
  const descriptor = text.slice(separatorIndex + 1).trim();
  return {
    raw: text,
    name,
    descriptor,
    options: descriptor.includes('|') ? descriptor.split('|').map((item) => item.trim()).filter(Boolean) : [],
  };
}

function extractPlaceholders(content) {
  const seen = new Set();
  const values = [];
  const regex = /\{\{([^{}]+)\}\}/g;
  let match;
  while ((match = regex.exec(String(content || ''))) !== null) {
    const raw = String(match[1] || '').trim();
    if (raw && !seen.has(raw)) {
      seen.add(raw);
      values.push(raw);
    }
  }
  return values;
}

function formatSlotDescription(raw) {
  const placeholder = parsePlaceholder(raw);
  if (!placeholder.descriptor) return 'Required single fill-in.';
  if (placeholder.descriptor === '0..N') return 'Repeatable block or collection. Replace with zero or more valid entries.';
  if (placeholder.descriptor === '0..1' || placeholder.descriptor.toLowerCase() === 'optional') {
    return 'Optional value or block. Remove the surrounding optional region when omitted.';
  }
  if (placeholder.options.length > 1) {
    return `Select one allowed value: ${placeholder.options.map((value) => `\`${value}\``).join(', ')}.`;
  }
  if (placeholder.name.endsWith('_JSON') || placeholder.descriptor.toLowerCase().includes('json')) {
    return 'Replace with valid JSON that matches the surrounding structure.';
  }
  if (placeholder.name.endsWith('_BODY') || placeholder.name.endsWith('_BLOCK')) {
    return 'Replace with multiline markdown, code, or repeated content that matches the surrounding structure.';
  }
  return `Fill using \`${placeholder.descriptor}\` semantics.`;
}

function repeatBlock(slot, lines) {
  const base = String(slot || '').split(':')[0];
  return [
    `<!-- REPEAT {{${slot}}} -->`,
    ...lines,
    `<!-- END REPEAT ${base} -->`,
  ];
}

