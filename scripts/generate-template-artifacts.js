function withTrailingCommas(lines = []) {
  return lines.map((line, index) => (index < lines.length - 1 ? `${line},` : line));
}

function docManagedBlock(docType, templateName, extraProps = []) {
  const props = [
    `  "docType": "${docType}"`,
    '  "version": {{DOC_VERSION:1}}',
    `  "templateName": "${templateName}"`,
    '  "templateVersion": "{{TEMPLATE_VERSION}}"',
    '  "sourceOfTruth": "{{SOURCE_OF_TRUTH:database|generated|hybrid}}"',
    ...extraProps,
  ];
  return [
    '<!-- APM:DATA',
    '{',
    ...props.map((line, index) => index < props.length - 1 ? `${line},` : line),
    '}',
    '-->',
  ];
}

