function fragmentMergeModeForDocType(docType) {
  const normalized = String(docType || '').trim().toLowerCase();
  if (OPERATION_DRIVEN_FRAGMENT_DOC_TYPES.has(normalized)) return 'apm-operations';
  if (normalized === 'database_schema_fragment') return 'database-import';
  return 'managed-body';
}

